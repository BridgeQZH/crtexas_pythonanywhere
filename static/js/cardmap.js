/**
 * 将 0‑119 的卡牌编号转换成 Clash Royale deck 密钥。
 * @param {number|string} id – 0‑119
 * @returns {string} 8‑位密钥；若找不到则返回 "00000000" 占位
 */
export function encode(id) {
  const idx = Number(id);
  return (idx >= 0 && idx < idMapping.length) ? idMapping[idx] : "00000000";
}

// 如果直接在 <script> 标签里引用而非模块化，也暴露到全局
if (typeof window !== 'undefined') {
  window.idMapping = idMapping;
  window.encode = encode;
}
